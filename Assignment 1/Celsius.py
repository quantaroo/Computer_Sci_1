#Assignment 1 Celsius

#Ask the user to input the Celsius
celsius = float(input("What is the temperature in Celsius? "))

#Calculate the Fahrenheit
fahrenheit = (celsius * (9/5)) + 32

#Print the result
print("With the given temperature:", celsius,"C")
print("The temperature in Fahrenheit is:", fahrenheit,"F")
